comando = input()
traducao = ""

if (comando == "Olha a chuva!"):
    traducao = "Grite choveu!"

elif (comando == "Olha a cobra!"):
    traducao = "Grite é mentira"

elif (comando == "Cavalheiros ajoelhados de frente pras damas!"):
    traducao = "Ajoelhe-se na frente da dama, beije a mão dela e grite beijou!"

elif (comando == "Alavantu!"):
    traducao = "Vá em direção a sua dama"

elif (comando == "Anarriê!"):
    traducao = "Se distancie da sua dama"

elif (comando == "Balancê!"):
    traducao = "Dance com a sua dama"

print(traducao)